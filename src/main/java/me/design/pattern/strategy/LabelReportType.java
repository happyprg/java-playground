package me.design.pattern.strategy;

public enum LabelReportType {

    TypeA(1, "typeA"),
    TypeB(2, "typeB");

    private Integer code;
    private String type;

    LabelReportType(Integer code, String type) {
        this.code = code;
        this.type = type;
    }

    public Integer getCode() {
        return code;
    }

    public String getType() {
        return type;
    }
}
